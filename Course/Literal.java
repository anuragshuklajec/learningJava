public class Literal {
    public static void main(String[] args) {
        // literals
        // binary numbers
        int num1 = 0b101;
        System.out.println(num1);
        // hexadecimal
        int num2 = 0x7E;
        System.out.println(num2);
        // you can even use _ between numbers for yout convience
        int num3 = 10__00__00_000;
        System.out.println(num3);
        // e representation for large numbers
        double num4 = 12e10;
        System.out.println(num4);
        


    }
}
