public class Loops {
    public static void main(String[] args) {
        // While loop
        int i = 0;
        while(i < 4){
            System.out.println(i);
            i++;
        }

        // Do while loop
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 4);

        // For loop
        for(int k = 0; k < 4; k++){
            System.out.println(k);
        }

    }
    
}
